import 'package:fpdart/fpdart.dart';
import 'package:delivery_app/core/constant/app_url.dart';
import 'package:delivery_app/core/failures/repo_failure.dart';
import 'package:delivery_app/core/network/api_header.dart';
import 'package:delivery_app/core/network/network.dart';
import 'package:delivery_app/modules/app/data/models/base_json.dart';
import 'package:delivery_app/modules/auth/data/datasource/login_remote_data_source.dart';
import 'package:delivery_app/modules/auth/data/model/response/login_model/login_model.dart';
import 'package:delivery_app/modules/auth/domain/params/login_param.dart';

class LoginRemoteDataSourceImpl implements LoginRemoteDataSource {
  final Network network;
  final AppUrl appUrl;

  LoginRemoteDataSourceImpl(this.network, this.appUrl);

  @override
  Future<Either<RepoFailure, BaseJson<LoginModel>>> login(LoginParam data) =>
      network
          .get(
            AppUrl.loginUrl,
            query: data.toModel().toJson(),
            ApiHeader.bearerHeaderWithApplicationJson(data.token),
          )
          .then(
            (value) => value.fold((l) => left(RepoFailure(error: l.error)), (
              response,
            ) {
              try {
                return right(
                  BaseJson<LoginModel>.fromJson(
                    response,
                    (res) => LoginModel.fromJson(res['data']),
                  ),
                );
              } catch (e) {
                return left(RepoFailure(error: e.toString()));
              }
            }),
          );
}
